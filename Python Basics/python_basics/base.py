# datatypes in python

# string/str : is a collection of characters
#  eg: name, location, profession
print("Amjath Hassan")

# Integer/int : Number without decimals
# eg: age, phone number, adhar card number
print(3)

# type() -> check data type
print(type(3))

# float/float : a number with decimal value
# weight, heigth, mark
print(5.5)
print(type(5.5))

# complex : number with real and imaginary part
# 2 + 3j : 2 -> real part, 3 -> imaginary part
print(2+3j)
print(type(2+3j)) # j is used to represent complex (mandatory)

# boolen : data exists in the form true or false
print(10<100)
print(10>100)
print(True) # Recerved word
print(type(True)) # <class 'bool'>

